#include "3-calc.h"

/**
 * op_add - adding two integers
 * @a: first int
 * @b: second int
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - supstraction
 * @a: first int
 * @b: second int
 *
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multipling
 * @a: first int
 * @b: second int
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divition
 * @a: first int
 * @b: second int
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder
 * @a: first int
 * @b: second int
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
